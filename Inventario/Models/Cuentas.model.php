<?php
require_once('../Config/cls_conexion.model.php');
class Clase_Cuentas
{
    public function todos()
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "SELECT cuentas.*, bancos.Nombre AS Nombres FROM `cuentas` INNER JOIN bancos on cuentas.ID_banco = bancos.ID_banco";
            $result = mysqli_query($con, $cadena);
            return $result;
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function uno($ID_cuenta)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "SELECT * FROM `cuentas` WHERE ID_cuenta=$ID_cuenta";
            $result = mysqli_query($con, $cadena);
            return $result;
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function insertar($ID_banco, $Tipo_cuenta, $Saldo)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "INSERT INTO `cuentas`( `ID_banco`,`Tipo_cuenta`, `Saldo`, `Fecha_apertura`) VALUES ('$ID_banco', '$Tipo_cuenta',$Saldo,CURDATE())";

            $result = mysqli_query($con, $cadena);

            return 'ok';
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function actualizar($ID_cuenta, $ID_banco, $Tipo_cuenta, $Saldo)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "UPDATE `cuentas` SET  `ID_banco`='$ID_banco',`Tipo_cuenta`='$Tipo_cuenta',`Saldo`=$Saldo,`Fecha_apertura`=CURDATE() WHERE `ID_cuenta`=$ID_cuenta";
            $result = mysqli_query($con, $cadena);
            return "ok";
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
    public function eliminar($ID_cuenta)
    {
        try {
            $con = new Clase_Conectar_Base_Datos();
            $con = $con->ProcedimientoConectar();
            $cadena = "delete from cuentas where ID_cuenta=$ID_cuenta";
            $result = mysqli_query($con, $cadena);
            return "ok";
        } catch (Throwable $th) {
            return $th->getMessage();
        } finally {
            $con->close();
        }
    }
}
